/** A single version snapshot of an artifact */
export type ArtifactVersion = {
  version: number;
  content: string;
  createdAt: string;
};

/** Artifact generated by the assistant */
export type Artifact = {
  id: string;
  type:
    | "code"
    | "excel"
    | "pptx"
    | "pdf"
    | "image"
    | "markdown"
    | "mermaid"
    | "csv"
    | "html"
    | "jsx"
    | "docx";
  title: string;
  content: string;
  language?: string;
  url?: string;
  createdAt: string;
  /** Version history (latest first) */
  versions?: ArtifactVersion[];
  /** Current version number (1-based) */
  currentVersion?: number;
  /** Workspace metadata for sandbox-generated files */
  workspace?: {
    workspaceId: string;
    filename: string;
  };
};

// Excel types
