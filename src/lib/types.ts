/** A chunk of agent output (streaming or stored) */
export type AgentChunk = {
  type:
    | "user"
    | "ask"
    | "thinking"
    | "tool_call"
    | "tool_call_args"
    | "tool_result"
    | "tool_call_pending"
    | "say"
    | "attempt_completion"
    | "assistant"
    | "completion"
    | "usage"
    | "tool_job_started"
    | "artifact";
  id: string;
  text?: string;
  created_at: string;
  user_id?: string;
  thinking?: string;
  tool_name?: string;
  tool_arguments?: string;
  tool_result?: string;
  tool_id?: string;
  args?: Record<string, unknown>;
  fire_and_forget?: boolean;
  content?: string;
  result?: string;
  is_finished?: boolean;
  isStreaming?: boolean;
  options?: string[];
  // usage fields
  prompt_tokens?: number;
  completion_tokens?: number;
  total_tokens?: number;
  total_cost?: number;
  // tool_job_started fields
  job_id?: string;
  provider?: string;
  // vision: image preview data URLs attached to user messages
  imageUrls?: string[];
  // artifact fields
  artifact_id?: string;
  content_type?: string;
  filename?: string;
  url?: string;
  size_bytes?: number;
  metadata?: Record<string, unknown>;
};

/** Inline attachment sent as base64 with the agent request */
export type InlineAttachment = {
  filename: string;
  content_type: string;
  data: string; // base64-encoded
};

